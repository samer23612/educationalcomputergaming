			<h2>Graduate Certificate in ECG</h2>
		
			<p>
				This certificate program has been honored in a rating by The Princeton Review in 2014 (https://www.stmarytx.edu/st-marys-among-top-graduate-schools-for-video-game-design).
				<br><br>
				In this certificate, you can learn how to evaluate, design, and create educational computer games for whatever platform you select.  You will make at least two (2) games (in CS6335 Game Development and in CS6345 eLearning and Gamification) as part of the required four (4) classes (<a href="http://catalog.stmarytx.edu/graduate/science-engineering-technology/computer-science/educational-computer-gaming-certificate/#requirementstext" title="http://catalog.stmarytx.edu/graduate/science-engineering-technology/computer-science/educational-computer-gaming-certificate/#requirementstext" target="_blank">www.stmarytx.edu/grad/gaming/admission</a>), for a total of 12 credit hours. 
				<br><br>
				The classes can count toward the Master's in Computer Science (<a href="https://www.stmarytx.edu/academics/set/graduate/computer-science/" target="_blank">www.stmarytx.edu/academics/set/graduate/computer-science/</a>) or the Master's in Computer Information Systems (<a href="https://www.stmarytx.edu/academics/set/graduate/cis/" target="_blank">www.stmarytx.edu/academics/set/graduate/cis/</a>) degrees at St. Mary's University.             
			
				There is a 4-course certificate program at St. Mary's University (<a href="www.stmarytx.edu" title="www.stmarytx.edu" target="_blank">www.stmarytx.edu</a>) in San Antonio, Texas, called Educational Computer Gaming (www.stmarytx.edu/grad/gaming).
				<ul class="a">
					<li style="list-style-type:none;"> The certificate program is for people who have</li>
					<br>
					<li>a bachelor's degree</li>
					<li>computer programming skills</li>
					<li>an interest in making educational computer games</li>
				</ul>
